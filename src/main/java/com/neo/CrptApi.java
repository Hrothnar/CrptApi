public class CrptApi {
    public static void main(String[] args) throws InterruptedException {
        Api api = new Api(TimeUnit.SECONDS, 5);
        for (int i = 0; i < 12; i++) {
            Document document = new Document();
            // fill up document object with data
            new Thread(() -> {
                api.createDocument(document, "signature");
            }).start();
        }
        Thread.sleep(1000 * 20);
        api.getScheduler().shutdown();
    }
}

class Api {
    private final int maxRequests;
    private int requestCount = 0;
    private final Semaphore semaphore = new Semaphore(1);
    private final ScheduledExecutorService scheduler = new ScheduledThreadPoolExecutor(1);

    public Api(TimeUnit unit, int maxRequests) {
        this.maxRequests = maxRequests;
        schedule(unit);
    }

    private void schedule(TimeUnit unit) {
        scheduler.scheduleAtFixedRate(() -> {
            requestCount = 0;
            System.out.println("The request counter has been reset");
        }, 5, 5, unit);
    }

    public ScheduledExecutorService getScheduler() {
        return scheduler;
    }

    public void createDocument(Document document, String signature) {
        try {
            semaphore.acquire();
            while (requestCount >= maxRequests) {
                System.out.println("Request limit has been reached. " + Thread.currentThread().getName() + " is waiting for the counter to reset");
                Thread.sleep(500);
            }
            ObjectMapper objectMapper = new ObjectMapper();
            String jsonBody = objectMapper.writeValueAsString(document);
            URL url = new URL("https://ismp.crpt.ru/api/v3/lk/documents/create");
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();
            connection.setRequestMethod("POST");
            connection.setRequestProperty("Content-Type", "application/json");
            connection.setDoOutput(true);
            int responseCode = connection.getResponseCode();
            System.out.println("Response code: " + responseCode);
            requestCount++;
        } catch (InterruptedException | IOException ex) {
            throw new RuntimeException(ex);
        } finally {
            semaphore.release();
        }
    }
}

